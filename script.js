const header = document.querySelector(".site-header");
const menuToggle = document.querySelector(".menu-toggle");
const nav = document.querySelector(".site-nav");
const navLinks = document.querySelectorAll(".site-nav a");
const revealItems = document.querySelectorAll("[data-reveal]");
const yearTarget = document.querySelector("[data-year]");
const langToggle = document.querySelector("[data-lang-toggle]");
const textNodes = document.querySelectorAll("[data-i18n]");
const altNodes = document.querySelectorAll("[data-i18n-alt]");
const ariaNodes = document.querySelectorAll("[data-i18n-aria-label]");
const metaDescription = document.querySelector("#meta-description");
const metaOgTitle = document.querySelector("#meta-og-title");
const metaOgDescription = document.querySelector("#meta-og-description");

let currentLanguage = "en";

if (yearTarget) {
  yearTarget.textContent = new Date().getFullYear();
}

const applyLanguage = (lang) => {
  const dictionary = translations[lang] || translations.en;

  currentLanguage = lang;
  document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";
  document.title = dictionary["meta.title"];

  if (metaDescription) {
    metaDescription.setAttribute("content", dictionary["meta.description"]);
  }

  if (metaOgTitle) {
    metaOgTitle.setAttribute("content", dictionary["meta.ogTitle"]);
  }

  if (metaOgDescription) {
    metaOgDescription.setAttribute("content", dictionary["meta.ogDescription"]);
  }

  textNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[key]) {
      node.textContent = dictionary[key];
    }
  });

  altNodes.forEach((node) => {
    const key = node.dataset.i18nAlt;
    if (dictionary[key]) {
      node.setAttribute("alt", dictionary[key]);
    }
  });

  ariaNodes.forEach((node) => {
    const key = node.dataset.i18nAriaLabel;
    if (dictionary[key]) {
      node.setAttribute("aria-label", dictionary[key]);
    }
  });

  window.localStorage.setItem("k1kora-language", lang);
};

const getPreferredLanguage = () => {
  const saved = window.localStorage.getItem("k1kora-language");

  if (saved && translations[saved]) {
    return saved;
  }

  if (navigator.language && navigator.language.toLowerCase().startsWith("zh")) {
    return "zh";
  }

  return "en";
};

applyLanguage(getPreferredLanguage());

if (langToggle) {
  langToggle.addEventListener("click", () => {
    applyLanguage(currentLanguage === "en" ? "zh" : "en");
  });
}

if (menuToggle && nav) {
  menuToggle.addEventListener("click", () => {
    const isOpen = nav.classList.toggle("is-open");
    menuToggle.setAttribute("aria-expanded", String(isOpen));
  });

  navLinks.forEach((link) => {
    link.addEventListener("click", () => {
      nav.classList.remove("is-open");
      menuToggle.setAttribute("aria-expanded", "false");
    });
  });
}

const updateHeaderState = () => {
  if (!header) {
    return;
  }

  header.classList.toggle("is-scrolled", window.scrollY > 16);
};

updateHeaderState();
window.addEventListener("scroll", updateHeaderState, { passive: true });

if ("IntersectionObserver" in window) {
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    {
      threshold: 0.18,
      rootMargin: "0px 0px -40px 0px",
    }
  );

  revealItems.forEach((item) => observer.observe(item));
} else {
  revealItems.forEach((item) => item.classList.add("is-visible"));
}
